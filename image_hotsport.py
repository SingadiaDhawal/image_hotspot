from transformers import OwlViTProcessor, OwlViTForObjectDetection
from PIL import Image, ImageDraw
import torch
import gradio as gr
import numpy as np

processor = OwlViTProcessor.from_pretrained("google/owlvit-base-patch32")
model = OwlViTForObjectDetection.from_pretrained("google/owlvit-base-patch32")

stored_boxes = []

labels = [
    "lamp",
    "chair",
    "table",
    "sofa",
    "bottle",
    "cup",
    "laptop",
    "phone",
    "book",
    "person",
    "tv",
    "plant",
    "fan",
    "bed",
    "clock"
]

def detect_objects(image):

    global stored_boxes

    stored_boxes = []

    image = image.convert("RGB")

    inputs = processor(
        text=[labels],
        images=image,
        return_tensors="pt"
    )

    with torch.no_grad():
        outputs = model(**inputs)

    target_sizes = torch.Tensor([image.size[::-1]])

    results = processor.post_process_object_detection(
        outputs=outputs,
        target_sizes=target_sizes,
        threshold=0.15
    )

    result = results[0]

    draw = ImageDraw.Draw(image)

    boxes = result["boxes"]
    scores = result["scores"]
    detected_labels = result["labels"]

    for box, score, label_idx in zip(boxes, scores, detected_labels):

        box = [round(i, 2) for i in box.tolist()]

        x1, y1, x2, y2 = map(int, box)

        label = labels[label_idx]

        stored_boxes.append({
            "label": label,
            "score": float(score),
            "x1": x1,
            "y1": y1,
            "x2": x2,
            "y2": y2
        })

        draw.rectangle(
            [(x1, y1), (x2, y2)],
            outline="red",
            width=3
        )

        draw.text(
            (x1, y1 - 15),
            f"{label} {score:.2f}",
            fill="red"
        )

    return image, stored_boxes

def hotspot_click(evt: gr.SelectData):

    x = evt.index[0]
    y = evt.index[1]

    for item in stored_boxes:

        if (
            item["x1"] <= x <= item["x2"] and
            item["y1"] <= y <= item["y2"]
        ):

            return f"""
Object Found

Name: {item['label']}
Confidence: {item['score']:.2f}

Coordinates:
({item['x1']}, {item['y1']})
({item['x2']}, {item['y2']})
"""

    return "No object detected"

with gr.Blocks() as demo:

    gr.Markdown("# Free AI Image Hotspot Detector")

